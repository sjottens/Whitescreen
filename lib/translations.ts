export type Locale = 'en' | 'nl' | 'es' | 'de' | 'fr' | 'it' | 'pt' | 'ja';
export type TranslationKey = keyof typeof translations.en;

export function getTranslation(locale: Locale, key: TranslationKey): string {
  // Fallback to English if locale translations don't exist
  const localeTranslations = translations[locale as keyof typeof translations];
  if (!localeTranslations) {
    return translations.en[key as keyof typeof translations.en] || key;
  }
  return (localeTranslations[key as keyof typeof localeTranslations] as string) || translations.en[key as keyof typeof translations.en] || key;
}

export function t(locale: Locale) {
  return (key: TranslationKey): string => getTranslation(locale, key);
}

